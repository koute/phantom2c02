function setBit(n1,n2)
{
	if ((n1 == -1) || (n2 == -1)) return;
	nodes[n1].gates.forEach(function(t){ turnTransistorOn(t);});
	nodes[n2].gates.forEach(function(t){ turnTransistorOff(t);});
	nodes[n1].state = true;
	nodes[n2].state = false;
	recalcNodeList([n1,n2]);
}

function palette_read(addr) {
	var r0 = 0;
	var r1 = 0;
	for (var b = 0; b < 6; b++)
	{
		var n0 = palette_nodes[addr][b][0];
		var n1 = palette_nodes[addr][b][1];
		if (isNodeHigh(n0))
			r0 |= 1 << b;
		if (isNodeHigh(n1))
			r1 |= 1 << b;
	}
	var r = r0 | r1;
	if (r != 0x3F)
		return 0xFF;
	else	return r1;
}

function palette_write(addr, val) {
	for (var b = 0; b < 6; b++)
	{
		var n0 = palette_nodes[addr][b][0];
		var n1 = palette_nodes[addr][b][1];
		if (val & (1 << b))
			setBit(n1, n0);
		else
			setBit(n0, n1);
	}
	if(animateChipLayout)
		refresh();
}

function sprite_read(addr) {
	var r0 = 0;
	var r1 = 0;
	for (var b = 0; b < 8; b++)
	{
		var n0 = sprite_nodes[addr][b][0];
		var n1 = sprite_nodes[addr][b][1];
		if ((n0 == -1) || (n1 == -1))
			continue;
		if (isNodeHigh(n0))
			r0 |= 1 << b;
		if (isNodeHigh(n1))
			r1 |= 1 << b;
	}
	return r1;
}

function sprite_write(addr, val) {
	for (var b = 0; b < 8; b++)
	{
		var n0 = sprite_nodes[addr][b][0];
		var n1 = sprite_nodes[addr][b][1];
		if (val & (1 << b))
			setBit(n1, n0);
		else
			setBit(n0, n1);
	}
	if(animateChipLayout)
		refresh();
}

var palette_show = true;
var palette_table;
var palette_selected;

function palette_setVisibility()
{
	palette_show = document.getElementById('showPalette').checked;
	palette_table.style.display = palette_show ? 'block' : 'none';
}

function palette_setupTable(){
	palette_table = document.getElementById('palette_table');
	for(var r = palette_table.rows.length; r > 0; r--)
		palette_table.deleteRow(r-1);
	for(var r=0;r<2;r++){
		var row = document.createElement('tr');
		palette_table.appendChild(row);
		var col = document.createElement('td');
		var addr = r;
		col.appendChild(document.createTextNode(hexWord(addr*16 + 0x3F00)+':'));
		col.onmousedown = palette_unselectCell;
		row.appendChild(col);
		for(var c=0;c<16;c++){
			col = document.createElement('td');
			col.addr = addr*16+c;
			col.val = palette_read(col.addr);
			col.onmousedown = function(e){palette_handleCellClick(e);};
			col.appendChild(document.createTextNode(hexByte(col.val)));
			row.appendChild(col);
		}
	}
	palette_setVisibility();
}

function palette_updateTable(cur)
{
	if (!palette_show) return;
	for (var c = 0; c < 0x20; c++)
	{
		if ((c != palette_selected) || cur)
			palette_setCellValue(c, palette_read(c));
	}
}

function palette_handleCellClick(e){
	var c = e.target;
	palette_selectCell(c.addr);
}

function palette_cellKeydown(e){
	var c = e.keyCode;
	var next = 0;
	if (palette_selected < 0x1F) next = palette_selected + 1;
	var prev = 0x1F;
	if (palette_selected > 0x00) prev = palette_selected - 1;
	if(c==13) palette_unselectCell();
	else if(c==32) palette_selectCell(next);
	else if(c==8) palette_selectCell(prev);
	else if((c>=48)&&(c<58)) palette_setCellValue(palette_selected, palette_getCellValue(palette_selected)*16+c-48);
	else if((c>=65)&&(c<71)) palette_setCellValue(palette_selected, palette_getCellValue(palette_selected)*16+c-55);
	palette_write(palette_selected, palette_getCellValue(palette_selected));
	palette_updateTable(false);
}

function palette_setCellValue(n, val){
	if(val==undefined)
		val=0x00;
	val%=256;
	palette_cellEl(n).val=val;
	palette_cellEl(n).innerHTML=hexByte(val);
}

function palette_getCellValue(n){return palette_cellEl(n).val;}

function palette_selectCell(n){
	palette_unselectCell();
	if (n >= 0x20) return;
	palette_cellEl(n).style.background = '#ff8';
	palette_selected = n;
	palette_table.onkeydown = function(e){palette_cellKeydown(e);};
}

function palette_unselectCell(){
	if(palette_selected==undefined) return;
	palette_cellEl(palette_selected).style.background = '#fff';
	palette_setCellValue(palette_selected, palette_read(palette_selected));
	palette_selected = undefined;
	window.onkeydown = undefined;
}

function palette_cellEl(n){
	var r = n>>4;
	var c = n%16;
	var e = palette_table.childNodes[r].childNodes[c+1];
	return e;
}

